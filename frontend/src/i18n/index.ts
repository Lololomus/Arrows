function applyDocumentLanguage(locale: AppLocale): void {
  document.documentElement.lang = locale;
}

export function normalizeLocale(rawLocale: unknown): AppLocale {
  if (typeof rawLocale !== 'string') {
    return 'en';
  }
  const normalized = rawLocale.trim().toLowerCase().replace('_', '-');
  return normalized.startsWith('ru') ? 'ru' : 'en';
}

export function detectTelegramLocale(): AppLocale {
  const tgLocale = (window as Window & {
    Telegram?: { WebApp?: { initDataUnsafe?: { user?: { language_code?: string } } } };
  }).Telegram?.WebApp?.initDataUnsafe?.user?.language_code;
  if (tgLocale) {
    return normalizeLocale(tgLocale);
  }
  return normalizeLocale(navigator.language);
}

export async function initI18n(initialLocale = detectTelegramLocale()): Promise<AppLocale> {
  const locale = normalizeLocale(initialLocale);

  if (!i18n.isInitialized) {
    await i18n
      .use(initReactI18next)
      .init({
        resources,
        lng: locale,
        fallbackLng: 'en',
        defaultNS: 'common',
        ns: [...NAMESPACES],
        interpolation: {
          escapeValue: false,
        },
      });
  } else if (i18n.resolvedLanguage !== locale) {
    await i18n.changeLanguage(locale);
  }

  applyDocumentLanguage(locale);
  return locale;
}

export async function setAppLocale(nextLocale: unknown): Promise<AppLocale> {
  const locale = normalizeLocale(nextLocale);
  if (!i18n.isInitialized) {
    return initI18n(locale);
  }
  if (i18n.resolvedLanguage !== locale) {
    await i18n.changeLanguage(locale);
  }
  applyDocumentLanguage(locale);
  return locale;
}

export function getAppLocale(): AppLocale {
  return normalizeLocale(i18n.resolvedLanguage || i18n.language || detectTelegramLocale());
}

export function exists(key: string): boolean {
  return i18n.exists(key);
}

export function translate(key: string, options?: Record<string, unknown>): string {
  const value = i18n.t(key, options as never);
  return typeof value === 'string' ? value : String(value);
}

export function formatNumber(value: number, options?: Intl.NumberFormatOptions): string {
  return new Intl.NumberFormat(getAppLocale(), options).format(value);
}

export function formatTimeUntil(targetIso: string | null, nowTs = Date.now()): string | null {
  if (!targetIso) return null;
  const targetTs = Date.parse(targetIso);
  if (!Number.isFinite(targetTs)) return null;
  const diffMs = targetTs - nowTs;
  if (diffMs <= 0) return null;

  const minutes = Math.ceil(diffMs / 60_000);
  const hours = Math.floor(minutes / 60);
  const remainingMinutes = minutes % 60;
  const minuteLabel = translate('common:units.minuteShort');
  const hourLabel = translate('common:units.hourShort');

  if (hours <= 0) {
    return `${Math.max(1, remainingMinutes)} ${minuteLabel}`;
  }
  return `${hours}${hourLabel} ${String(remainingMinutes).padStart(2, '0')}${minuteLabel}`;
}

